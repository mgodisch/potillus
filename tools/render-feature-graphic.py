# ── Pillow: build the phone upright and perspective-warp it to lean back ─────
def _solve8(rows: list[list[float]], rhs: list[float]) -> list[float]:
    """Solve an 8x8 linear system by Gaussian elimination with partial pivoting.

    Used only to derive the perspective-warp coefficients. Pure Python keeps the
    tool free of a numpy dependency and the result deterministic.
    """
    n = len(rhs)
    a = [rows[i][:] + [rhs[i]] for i in range(n)]
    for col in range(n):
        piv = max(range(col, n), key=lambda r: abs(a[r][col]))
        a[col], a[piv] = a[piv], a[col]
        pivot = a[col][col]
        for j in range(col, n + 1):
            a[col][j] /= pivot
        for r in range(n):
            if r != col and a[r][col]:
                factor = a[r][col]
                for j in range(col, n + 1):
                    a[r][j] -= factor * a[col][j]
    return [a[i][n] for i in range(n)]


def _perspective_coeffs(dst, src):
    """The 8 PIL PERSPECTIVE coefficients that map each OUTPUT point in `dst` to
    the INPUT point in `src`, so the source rectangle is displayed inside the dst
    quadrilateral."""
    m: list[list[float]] = []
    b: list[float] = []
    for (X, Y), (x, y) in zip(dst, src):
        m.append([X, Y, 1, 0, 0, 0, -x * X, -x * Y]); b.append(x)
        m.append([0, 0, 0, X, Y, 1, -y * X, -y * Y]); b.append(y)
    return _solve8(m, b)


def _render_phone_png(today_png: Path, w: int, h: int, r: int, bezel: int) -> bytes:
    """Build the phone (rounded bezel + screenshot + island) UPRIGHT, then
    perspective-warp it so it turns slightly about its VERTICAL axis -- the LEFT
    edge recedes (farther, shorter) and the RIGHT edge stays near -- and return the
    RGBA PNG bytes.

    SVG transforms are affine and cannot express perspective, so this single step
    is done in Pillow; the warped phone is then embedded as one image in the SVG.
    It is rendered at SS x the on-canvas size for crisp downscaling and is
    deterministic for a given Pillow version and inputs.
    """
    ss = 3
    wp, hp = w * ss, h * ss
    rad, bz, scr_r = r * ss, bezel * ss, (r - bezel) * ss

    phone = Image.new("RGBA", (wp, hp), (0, 0, 0, 0))
    ImageDraw.Draw(phone).rounded_rectangle(
        [0, 0, wp - 1, hp - 1], radius=rad,
        fill=(5, 8, 13, 255), outline=(39, 49, 63, 255), width=max(1, ss),
    )

    # Screen: cover-scale the screenshot to the inner area, centred horizontally
    # and top-aligned (matching the previous xMidYMin "slice"), clipped to a
    # rounded rect.
    sw, sh = wp - 2 * bz, hp - 2 * bz
    shot = Image.open(today_png).convert("RGBA")
    scale = max(sw / shot.width, sh / shot.height)
    shot = shot.resize(
        (max(1, round(shot.width * scale)), max(1, round(shot.height * scale))),
        Image.LANCZOS,
    )
    left = (shot.width - sw) // 2
    shot = shot.crop((left, 0, left + sw, sh))
    mask = Image.new("L", (sw, sh), 0)
    ImageDraw.Draw(mask).rounded_rectangle([0, 0, sw - 1, sh - 1], radius=scr_r, fill=255)
    phone.paste(shot, (bz, bz), mask)

    # Dynamic-island pill near the top of the screen.
    iw, ih = 44 * ss, 12 * ss
    ix, iy = (wp - iw) // 2, 14 * ss
    ImageDraw.Draw(phone).rounded_rectangle(
        [ix, iy, ix + iw, iy + ih], radius=ih // 2, fill=(5, 8, 13, 255),
    )

    # Lean back about the VERTICAL axis: the LEFT edge recedes (moves inward and
    # is shortened top+bottom), while the RIGHT edge stays full and near. dst is
    # the output trapezoid, src the upright rect (corners TL, TR, BR, BL).
    lx, ly = round(wp * 0.06), round(hp * 0.03)
    dst = [(lx, ly), (wp, 0), (wp, hp), (lx, hp - ly)]
    src = [(0, 0), (wp, 0), (wp, hp), (0, hp)]
    coeffs = _perspective_coeffs(dst, src)
    warped = phone.transform(
        (wp, hp), Image.PERSPECTIVE, coeffs,
        resample=Image.BICUBIC, fillcolor=(0, 0, 0, 0),
    )

    buf = io.BytesIO()
    warped.save(buf, format="PNG", optimize=True)
    return buf.getvalue()


def _build_svg(copy: Copy, today_png: Path, report_png: Path, badge_svg: Path,
               cjk_family: str | None = None) -> str:
    """Assemble the full SVG document string for one locale.

    `cjk_family` (see _cjk_family_for_locale) is appended after Inter in the
    text font-family for CJK locales; None keeps the Latin-only Inter family.
    """
    font_family = _font_family(cjk_family)
    report_uri = _data_uri(report_png)
    icon_uri = _data_uri(ICON_FG)

    parts: list[str] = []
    parts.append(
        f'<svg xmlns="http://www.w3.org/2000/svg" '
        f'xmlns:xlink="http://www.w3.org/1999/xlink" '
        f'width="{W}" height="{H}" viewBox="0 0 {W} {H}">'
    )

    # ── Defs: gradients, clips, shadow filter ────────────────────────────────
    parts.append("<defs>")
    parts.append(
        f'<linearGradient id="bg" x1="0" y1="0" x2="1" y2="1">'
        f'<stop offset="0" stop-color="{BG_TOP}"/>'
        f'<stop offset="1" stop-color="{BG_BOTTOM}"/>'
        f'</linearGradient>'
    )
    parts.append(
        f'<radialGradient id="glow" cx="0.5" cy="0.5" r="0.5">'
        f'<stop offset="0" stop-color="{GLOW}" stop-opacity="0.42"/>'
        f'<stop offset="0.55" stop-color="{GLOW}" stop-opacity="0.12"/>'
        f'<stop offset="1" stop-color="{GLOW}" stop-opacity="0"/>'
        f'</radialGradient>'
    )
    # Gradient for the phone's right-side depth edge: lighter where it meets the
    # front face (a metal highlight), darkening toward the back.
    parts.append(
        '<linearGradient id="edge" x1="0" y1="0" x2="1" y2="0">'
        '<stop offset="0" stop-color="#3a4759"/>'
        '<stop offset="0.5" stop-color="#10151e"/>'
        '<stop offset="1" stop-color="#05080d"/>'
        '</linearGradient>'
    )
    # Phone geometry. The phone is built and perspective-warped with Pillow (see
    # _render_phone_png) and embedded as a single image, so there is no SVG screen
    # clip here. ph_r/bezel drive the warp; ph_x/ph_y place the result; bullets are
    # centred on ph_y + ph_h/2.
    ph_x, ph_y, ph_w, ph_h, ph_r = 598, 52, 196, 396, 18
    bezel = 6  # bezel thickness (inset from body edge to screen)
    parts.append(
        '<filter id="shadow" x="-30%" y="-30%" width="160%" height="160%">'
        '<feDropShadow dx="0" dy="6" stdDeviation="10" '
        'flood-color="#000000" flood-opacity="0.45"/></filter>'
    )
    # Icon tile clip.
    parts.append(
        '<clipPath id="iconclip"><rect x="48" y="54" width="86" height="86" '
        'rx="20" ry="20"/></clipPath>'
    )
    parts.append("</defs>")

    # ── Background + glow ────────────────────────────────────────────────────
    parts.append(f'<rect width="{W}" height="{H}" fill="url(#bg)"/>')
    parts.append(
        '<ellipse cx="696" cy="250" rx="350" ry="300" fill="url(#glow)"/>'
    )

    # NOTE: the report "paper" and the phone are drawn LAST (see end of this
    # function), so the phone overlaps the right edge of the feature boxes.

    # ── Left column: icon tile + title + taglines ────────────────────────────
    parts.append(
        f'<rect x="48" y="54" width="86" height="86" rx="20" ry="20" '
        f'fill="{ICON_BG}"/>'
    )
    parts.append(
        f'<image x="48" y="54" width="86" height="86" clip-path="url(#iconclip)" '
        f'xlink:href="{icon_uri}"/>'
    )
    # White frame so the (dark) icon tile separates from the dark background,
    # mirroring the reference graphic. Drawn LAST so it sits above the icon.
    parts.append(
        '<rect x="48" y="54" width="86" height="86" rx="20" ry="20" fill="none" '
        'stroke="#ffffff" stroke-opacity="0.9" stroke-width="2.5"/>'
    )

    title_y = 196
    parts.append(
        f'<text x="48" y="{title_y}" font-family="{font_family}" font-weight="700" '
        f'font-size="30" letter-spacing="0.4" fill="{TEXT_PRIMARY}">'
        f'{_esc(copy.title)}</text>'
    )

    # Taglines: each paragraph wrapped to the left column width.
    tag_size = 21
    tag_lh = 28
    y = title_y + 46
    for para in copy.taglines:
        lines = _wrap(para, tag_size, max_width=320)
        parts.append(
            f'<text x="48" y="{y:.1f}" font-family="{font_family}" font-weight="400" '
            f'font-size="{tag_size}" fill="{TEXT_SECONDARY}">'
            f'{_tspans(lines, 48, y, tag_lh)}</text>'
        )
        y += tag_lh * len(lines) + 18  # paragraph gap

    # Bottom corners: the "Get it on F-Droid" badge (original colours) in the
    # bottom-LEFT and the GPLv3 "Free as in Freedom" logo (recoloured WHITE) in
    # the bottom-RIGHT, each inset by EQUAL bottom + side margins (48 px) so the
    # two corners mirror one another. Both share the same bottom baseline and the
    # same VISIBLE height.
    #
    # logo_w drives that shared height (the badge is matched to the logo's visible
    # height). It is kept at 96 -- the reduced size introduced earlier -- so the
    # pair renders a little smaller than the logo's native 112. NOTE: the original
    # phone-clearance reason for the reduction no longer strictly applies now that
    # the WIDE badge sits bottom-LEFT (clear of the phone) and the NARROW logo sits
    # bottom-RIGHT (its left edge x≈880 clears the phone body + soft shadow ≈823 by
    # ~57 px); the reduced size is retained by choice and may be raised to 112.
    margin = 48.0
    logo_w = 96.0
    logo_h = logo_w * 358.0 / 720.0
    baseline_y = H - margin - logo_h
    # F-Droid badge, bottom-LEFT (x = margin): original brand colours, cropped to
    # its ink box and scaled to the shared visible height (see _badge_nested). The
    # bottom-left corner is clear of the phone and the PDF "paper", so it is drawn
    # here in normal order.
    parts.append(_badge_nested(badge_svg, margin, baseline_y, logo_h))

    # The GPLv3 logo mirrors this into the bottom-RIGHT corner, but it is appended
    # LATER -- after the report "paper" -- so it sits IN FRONT of the tilted PDF
    # screenshot instead of behind it. See the report/phone block below.
    # ── Centre column: four feature bullets ──────────────────────────────────
    # The boxes are wide enough that their RIGHT edge (bx+bw) tucks behind the
    # phone (drawn afterwards), as in the reference; the label column is capped
    # (lab_max) so wrapped text never runs under the phone. Every box shares ONE
    # height, sized to fit the TALLEST label (most lines) with EQUAL padding above
    # and below the text block; the whole stack is then centred on the phone, which
    # leaves a little more breathing room above and below it.
    bx, bw = 372, 268
    bgap = 13
    lab_size = 18
    lab_lh = 22
    lab_max = 150
    cap_h = 0.72 * lab_size      # approx. cap height (baseline -> cap top)
    descent = 0.21 * lab_size    # approx. descender depth
    pad = 9                      # equal padding above and below the text block
    wrapped = [_wrap(t, lab_size, max_width=lab_max) for t in copy.bullets]
    max_lines = max(len(w) for w in wrapped)
    text_h = cap_h + (max_lines - 1) * lab_lh + descent
    bh = text_h + 2 * pad
    stack_h = 4 * bh + 3 * bgap
    by = (ph_y + ph_h / 2) - stack_h / 2
    for icon, lines in zip(_BULLET_ICONS, wrapped):
        box_cy = by + bh / 2
        parts.append(
            f'<rect x="{bx}" y="{by:.1f}" width="{bw}" height="{bh:.1f}" rx="16" '
            f'ry="16" fill="{BULLET_BG}" stroke="{BULLET_STROKE}" stroke-width="1"/>'
        )
        # Icon: 24-unit glyph scaled to ~30px, centred on the box centre line.
        parts.append(
            f'<g transform="translate({bx + 18:.1f} {box_cy - 15:.1f}) '
            f'scale({30 / 24:.4f})" color="{ACCENT}">{icon}</g>'
        )
        # Label: its VISUAL centre is aligned to the box centre, so a single-line
        # label sits level with the icon, and a multi-line label keeps EQUAL padding
        # above and below (using cap-height/descent rather than full line slots,
        # which is what previously pushed the text up and left a gap underneath).
        first_baseline = box_cy - (len(lines) - 1) * lab_lh / 2 + (cap_h - descent) / 2
        parts.append(
            f'<text x="{bx + 60}" y="{first_baseline:.1f}" font-family="{font_family}" '
            f'font-weight="600" font-size="{lab_size}" fill="{TEXT_PRIMARY}">'
            f'{_tspans(lines, bx + 60, first_baseline, lab_lh)}</text>'
        )
        by += bh + bgap

    # ── Report "paper" + phone, drawn LAST so the phone overlaps the boxes ────
    # Report page: gently tilted (~10 deg, a touch more upright than before),
    # top toward the upper right, fully inside the canvas.
    pa_w, pa_h = 206, 292
    pa_cx, pa_cy, pa_angle = 838, 248, 10
    parts.append(
        f'<g transform="rotate({pa_angle} {pa_cx} {pa_cy})" filter="url(#shadow)">'
        f'<rect x="{pa_cx - pa_w/2:.1f}" y="{pa_cy - pa_h/2:.1f}" width="{pa_w}" '
        f'height="{pa_h}" rx="6" ry="6" fill="#ffffff"/>'
        f'<image x="{pa_cx - pa_w/2:.1f}" y="{pa_cy - pa_h/2:.1f}" width="{pa_w}" '
        f'height="{pa_h}" preserveAspectRatio="xMidYMid slice" '
        f'xlink:href="{report_uri}"/>'
        f'</g>'
    )

    # GPLv3 logo (bottom-right corner): drawn HERE, AFTER the report "paper", so it
    # OVERLAPS/covers the tilted PDF screenshot instead of being hidden behind it.
    # Recoloured WHITE and right-anchored at the mirrored 48 px margin, sharing the
    # bottom baseline and visible height with the bottom-left badge. Its left edge
    # (x≈880) clears the phone + soft shadow (≈823), so being placed before the
    # phone below is fine — they do not overlap.
    parts.append(_logo_nested(W - margin - logo_w, baseline_y, logo_w, color="#ffffff"))

    # Right-side depth edge: a thin perspective band along the phone's NEAR (right)
    # edge, giving it visible thickness (the front face is otherwise flat). It is
    # tapered (shorter at the back) and drawn just before the phone so the front
    # face sits flush in front of it.
    rim_x = ph_x + ph_w
    rim_top, rim_bot = ph_y + ph_r, ph_y + ph_h - ph_r
    rim_d, rim_t = 9, 7
    parts.append(
        f'<polygon points="{rim_x},{rim_top} {rim_x + rim_d},{rim_top + rim_t} '
        f'{rim_x + rim_d},{rim_bot - rim_t} {rim_x},{rim_bot}" '
        f'fill="url(#edge)" filter="url(#shadow)"/>'
    )

    # Phone: built upright (bezel + screenshot + island) and perspective-warped to
    # lean back, all in Pillow; embedded here as ONE image. Its content is a
    # trapezoid inside the ph_w x ph_h box (transparent margins), and the image
    # already has those proportions, so it is placed with preserveAspectRatio
    # "none". The drop shadow follows the warped (trapezoidal) alpha.
    phone_uri = "data:image/png;base64," + base64.b64encode(
        _render_phone_png(today_png, ph_w, ph_h, ph_r, bezel)
    ).decode("ascii")
    parts.append(
        f'<image x="{ph_x}" y="{ph_y}" width="{ph_w}" height="{ph_h}" '
        f'preserveAspectRatio="none" filter="url(#shadow)" '
        f'xlink:href="{phone_uri}"/>'
    )

    parts.append("</svg>")
    return "".join(parts)


# ── Rendering ────────────────────────────────────────────────────────────────
def _render_png(svg: str, out_png: Path, scale: int = 1) -> None:
    """Render the SVG to a PNG with rsvg-convert using ONLY the bundled fonts.

    The output is `scale` x the Play feature-graphic size, i.e.
    (1024*scale) x (500*scale) px. The viewBox is unchanged, so the whole graphic
    is simply rasterised at a higher pixel density: vector elements (text, the
    badge and its fonts, the logo, shapes) stay razor-sharp, and the embedded
    screenshots -- heavily downscaled even at scale=4 -- stay crisp too.

    Determinism hinges on the font: librsvg resolves font families through
    fontconfig, so we generate a throwaway fontconfig that exposes ONLY
    tools/fonts/ and point FONTCONFIG_FILE at it. The host's installed
    fonts are therefore irrelevant -- the same Inter files are always used.
    """
    with tempfile.TemporaryDirectory() as td:
        tdp = Path(td)
        svg_path = tdp / "feature.svg"
        svg_path.write_text(svg, encoding="utf-8")

        fc_cache = tdp / "fc-cache"
        fc_cache.mkdir()
        fc_conf = tdp / "fonts.conf"
        fc_conf.write_text(
            "<?xml version='1.0'?>"
            "<!DOCTYPE fontconfig SYSTEM 'fonts.dtd'>"
            "<fontconfig>"
            f"<dir>{FONT_DIR}</dir>"
            f"<cachedir>{fc_cache}</cachedir>"
            "</fontconfig>",
            encoding="utf-8",
        )
        env = dict(os.environ)
        env["FONTCONFIG_FILE"] = str(fc_conf)

        out_png.parent.mkdir(parents=True, exist_ok=True)
        subprocess.run(
            [
                "rsvg-convert",
                "--width", str(W * scale),
                "--height", str(H * scale),
                "--format", "png",
                "--output", str(out_png),
                str(svg_path),
            ],
            check=True,
            env=env,
        )


def render_locale(locale: str) -> tuple[Path, Path]:
    """Render the feature graphic for one locale.

    Returns ``(store_png, hq_png)``: the 1024x500 Play graphic and its
    high-resolution companion.
    """
    loc_dir = META_DIR / locale
    copy = Copy.load(loc_dir / "feature-graphic.txt")
    shots = loc_dir / "images" / "phoneScreenshots"
    today_png = shots / "01_today.png"
    report_png = shots / "07_report_page_1.png"
    badge_svg = _badge_for_locale(locale)
    cjk_family = _cjk_family_for_locale(locale)
    for p in (today_png, report_png, ICON_FG, badge_svg):
        if not p.is_file():
            raise FileNotFoundError(f"required input missing: {p}")
    svg = _build_svg(copy, today_png, report_png, badge_svg, cjk_family)
    images = loc_dir / "images"
    out_png = images / "featureGraphic.png"
    _render_png(svg, out_png)                       # 1024x500, uploaded to Play
    # High-resolution companion for press/web/print. fastlane supply only matches
    # the exact name featureGraphic.png, so this file is never uploaded (and Play
    # caps the store feature graphic at 1024x500 anyway).
    hq_png = images / "featureGraphic-4K.png"
    _render_png(svg, hq_png, scale=HQ_SCALE)
    return out_png, hq_png


def main(argv: list[str]) -> int:
    """CLI entry point: render the feature graphic for the requested locales."""
    ap = argparse.ArgumentParser(
        description="Render Play-Store feature graphics deterministically."
    )
    ap.add_argument(
        "locales",
        nargs="*",
        help="locale dirs under fastlane/metadata/android/ "
        "(default: every locale that has a feature-graphic.txt)",
    )
    ap.add_argument(
        "--svg-only",
        metavar="FILE",
        help="write the assembled SVG for the FIRST locale to FILE and skip "
        "rendering (useful for inspecting/debugging the markup)",
    )
    args = ap.parse_args(argv)

    if shutil.which("rsvg-convert") is None:
        print(
            "render-feature-graphic: 'rsvg-convert' not found -- install it "
            "(Debian: apt install librsvg2-bin).",
            file=sys.stderr,
        )
        return 1

    locales = args.locales or sorted(
        p.parent.name
        for p in META_DIR.glob("*/feature-graphic.txt")
    )
    if not locales:
        print("render-feature-graphic: no feature-graphic.txt files found.",
              file=sys.stderr)
        return 1

    if args.svg_only:
        loc = locales[0]
        loc_dir = META_DIR / loc
        copy = Copy.load(loc_dir / "feature-graphic.txt")
        shots = loc_dir / "images" / "phoneScreenshots"
        svg = _build_svg(copy, shots / "01_today.png", shots / "07_report_page_1.png",
                         _badge_for_locale(loc), _cjk_family_for_locale(loc))
        Path(args.svg_only).write_text(svg, encoding="utf-8")
        print(f"wrote SVG for {loc} -> {args.svg_only}")
        return 0

    for loc in locales:
        out, hq = render_locale(loc)
        print(f"feature graphic ({loc}) -> {out}")
        print(f"  high-res     ({loc}) -> {hq}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv[1:]))
