# render-feature-graphic.py -- deterministic Play-Store feature graphic
# =============================================================================
#
# WHAT THIS PRODUCES
#   One `featureGraphic.png` per locale, sized EXACTLY 1024x500 (the Google Play
#   feature-graphic requirement), written straight into the fastlane metadata
#   tree at:
#       fastlane/metadata/android/<locale>/images/featureGraphic.png
#
# WHY IT EXISTS
#   The previous feature graphics were generated by an image model. That made
#   their text impossible to edit cleanly and, worse, baked AI-hallucinated text
#   into the embedded "screenshots" (e.g. a garbled PDF report). This script
#   composes the graphic DETERMINISTICALLY from three inputs the project already
#   controls, so it is reproducible and trivially re-localizable:
#       1. per-locale marketing copy  -> fastlane/.../<locale>/feature-graphic.txt
#       2. the REAL, current screenshots produced by `make screenshots`
#          (01_today.png as the phone, 07_report_page_1.png as the report page)
#       3. the app's own launcher icon (foreground PNG + adaptive background)
#
# WHY SVG + rsvg-convert (and not Pillow)
#   The layout is markup (gradients, rounded rects, a rotated "paper", clipped
#   screenshots), which is far more legible and tweakable as SVG than as Pillow
#   pixel math. The one determinism caveat of librsvg -- that text rendering
#   depends on whatever fonts the host happens to have -- is removed by pinning a
#   bundled font: we point fontconfig at tools/fonts/ ONLY (see
#   _render_png), so the same Inter files are always used regardless of host.
#
# RUNTIME DEPENDENCIES (kept deliberately small)
#   * python3 standard library only (no third-party Python packages),
#   * rsvg-convert (Debian package `librsvg2-bin`),
#   * the bundled fonts under tools/fonts/Inter/.
#   The static Inter instances were prepared once with fontTools; this script
#   never needs fontTools itself.
#
# TEXT WRAPPING
#   Marketing copy is wrapped here so the per-locale .txt files stay free of
#   layout concerns -- editors change words, not line breaks. Wrapping uses an
#   approximate advance-width model (see _text_width); it is intentionally
#   slightly conservative so a line never overflows its column. An explicit
#   line break can still be forced by writing a literal '\n' inside a value.
# =============================================================================

